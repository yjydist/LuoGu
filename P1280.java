import java.util.Arrays;
import java.util.Scanner;

/**
 * 洛谷 P1280 尼克的任务
 *
 * 题意：
 * 尼克的工作日为 n 分钟（从第 1 分钟到第 n 分钟），有 k 个任务。
 * 每个任务有开始时间 p 和持续时间 t，即从第 p 分钟开始，做到第 p+t-1 分钟结束。
 *
 * 规则：
 * 1. 某时刻没有任务 → 尼克空闲
 * 2. 某时刻只有一个任务 → 尼克必须做
 * 3. 某时刻有多个任务 → 尼克任选一个做，其余同事做
 * 4. 某时刻有任务开始但尼克正在工作中 → 这些任务交给同事
 *
 * 目标：求尼克能获得的最大空闲时间。
 */
public class P1280 {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        // n: 工作日总分钟数
        // k: 任务总数
        int n = sc.nextInt();
        int k = sc.nextInt();

        // tasks[i][0] = 第 i 个任务的开始时间 p
        // tasks[i][1] = 第 i 个任务的持续时间 t
        int[][] tasks = new int[k][2];

        // cnt[i] 表示第 i 分钟有几个任务开始
        // 用于快速判断：走到第 i 分钟时，需不需要处理任务
        int[] cnt = new int[n + 2];

        for (int i = 0; i < k; i++) {
            tasks[i][0] = sc.nextInt(); // 读入开始时间 p
            tasks[i][1] = sc.nextInt(); // 读入持续时间 t
            cnt[tasks[i][0]]++;          // 统计第 p 分钟开始的任务数量
        }
        sc.close();

        // ============================================================
        // 关键步骤 1：将任务按开始时间从大到小排序（降序）
        // ============================================================
        // 为什么要降序排序？
        // 因为 DP 是从后往前（从第 n 分钟倒推到第 1 分钟）进行的。
        // 当我们倒序遍历到第 i 分钟时，需要处理所有从第 i 分钟开始的任务。
        // 如果任务按开始时间降序排好，那么我们就可以用一个指针 idx 顺序遍历 tasks 数组，
        // 刚好遇到的所有任务都是从大到小按开始时间排列的。
        // 这样不需要每次都在所有任务中查找哪些是从第 i 分钟开始的。
        //
        // 举例：
        // 排序前任务开始时间：[1, 8, 4, 11, 1, 8]
        // 排序后任务开始时间：[11, 8, 8, 4, 1, 1]
        // 倒序遍历时，i=11 先遇到开始时间为 11 的任务
        // i=8 再遇到两个开始时间为 8 的任务，以此类推。
        Arrays.sort(tasks, (a, b) -> b[0] - a[0]);

        // ============================================================
        // 关键步骤 2：定义 dp 数组
        // ============================================================
        // dp[i] 表示：从第 i 分钟到第 n 分钟，尼克能获得的最大空闲时间。
        //
        // 为什么倒着定义？
        // 如果正着定义（dp[i] = 前 i 分钟的最大空闲时间），会有"后效性"：
        // 前面选了某个任务，会占用一段时间，这段时间内开始的任务就自动被跳过，
        // 这会影响后面的可选任务集合。前面的决策会改变后面的状态，无法简单递推。
        //
        // 倒着定义就消除了后效性：
        // 从第 i 分钟往后看，后面的所有决策已经确定了，不会受前面的影响。
        int[] dp = new int[n + 2];
        // dp 数组默认初始化为 0，dp[n+1] = 0 表示"第 n 分钟之后"没有空闲时间。

        // idx: 当前处理到排序后的第几个任务
        int idx = 0;

        // ============================================================
        // 关键步骤 3：倒序 DP
        // ============================================================
        // 从第 n 分钟倒推到第 1 分钟，逐个计算 dp[i]。
        for (int i = n; i >= 1; i--) {

            if (cnt[i] == 0) {
                // --------------------------------------------------------
                // 情况 A：第 i 分钟没有任务开始
                // --------------------------------------------------------
                // 尼克这一分钟是空闲的，所以空闲时间 = 下一分钟的空闲时间 + 这一分钟
                //
                // 例如：dp[15] = dp[16] + 1 = 0 + 1 = 1
                //      表示从第 15 分钟到结束，最多空闲 1 分钟（就是第 15 分钟本身）
                dp[i] = dp[i + 1] + 1;

            } else {
                // --------------------------------------------------------
                // 情况 B：第 i 分钟有任务开始（可能有多个）
                // --------------------------------------------------------
                // 根据题意，只要第 i 分钟有任务，尼克就必须接一个（一个时强制接，多个时任选一个）。
                // 如果接了一个持续时间为 t 的任务，那么从第 i 分钟到第 i+t-1 分钟他都在工作，
                // 下一个可以休息或选择的时刻是第 i+t 分钟。
                //
                // 所以我们要在所有从第 i 分钟开始的任务中选一个，
                // 使得接完任务后的空闲时间最大：
                //      dp[i] = max(dp[i + t_j])   对所有从第 i 分钟开始的任务 j
                //
                // 举例：
                // 第 8 分钟有两个任务：(8, 5) 和 (8, 1)
                // 如果接 (8, 5)，做完后跳到第 13 分钟，空闲时间 = dp[13]
                // 如果接 (8, 1)，做完后跳到第 9 分钟，空闲时间 = dp[9]
                // dp[8] = max(dp[13], dp[9])
                for (int j = 0; j < cnt[i]; j++) {
                    int duration = tasks[idx][1]; // 当前任务的持续时间 t
                    dp[i] = Math.max(dp[i], dp[i + duration]);
                    idx++; // 处理下一个任务（排序后下一个任务的 start 时间 <= 当前）
                }
            }
        }

        // ============================================================
        // 输出答案
        // ============================================================
        // dp[1] 表示从第 1 分钟到第 n 分钟能获得的最大空闲时间，就是最终答案。
        System.out.println(dp[1]);
    }
}
