import java.util.Scanner;

/**
 * 洛谷 P1226：【模板】快速幂
 *
 * 题目要求：给定 a, b, p，求 a^b mod p
 *
 * 为什么不能用普通方法？
 * 1. 时间：如果 b 很大（比如 10^9），循环乘 b 次会超时
 * 2. 空间/精度：a^b 的结果可能极其巨大，long 存不下，double 精度不够
 *
 * 解决方案：快速幂（反复平方法）
 * 核心思想：把指数 b 拆成二进制，通过"平方"来跳过大量乘法
 */
public class P1226 {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        // 读入三个数，用 long 防止中间计算溢出
        long a = sc.nextLong();  // 底数
        long b = sc.nextLong();  // 指数
        long p = sc.nextLong();  // 模数
        sc.close();

        // 调用快速幂函数，按题目格式输出
        // 注意：a 先对 p 取模，因为 (a^b) % p == ((a%p)^b) % p
        long ans = fastPow(a % p, b, p);
        System.out.println(a + "^" + b + " mod " + p + "=" + ans);
    }

    /**
     * 快速幂：计算 (a^b) % p
     *
     * 核心原理：
     * 任何一个正整数 b 都可以表示为二进制形式。
     * 例如 b = 13，二进制是 1101，即 13 = 8 + 4 + 1 = 2^3 + 2^2 + 2^0
     * 所以 a^13 = a^(8+4+1) = a^8 * a^4 * a^1
     *
     * 注意到：a^1, a^2, a^4, a^8 ... 每一项都是前一项的平方
     * 也就是说，我们只需要不断把底数平方，就能依次得到 a^1, a^2, a^4, a^8...
     *
     * 然后检查 b 的每一位（从低位到高位），如果是 1，就把对应的 a^(2^i) 乘入答案
     *
     * @param a 底数（已经预处理过 a % p）
     * @param b 指数（非负整数）
     * @param p 模数（正整数）
     * @return (a^b) % p 的结果
     */
    static long fastPow(long a, long b, long p) {
        // res 保存最终答案，初始值为 1
        // 数学上任何数的 0 次方都是 1，所以初始值是 1
        // 这里写 1 % p 是为了处理 p = 1 的特殊情况（此时任何数 % 1 都是 0）
        long res = 1 % p;

        // 当 b > 0 时继续循环，每次处理 b 的最低一位二进制位
        while (b > 0) {

            // ---------- 步骤1：判断当前最低位是否为 1 ----------
            // b & 1 表示取 b 的二进制最低位
            // 如果结果是 1，说明当前这一位是 1，需要把当前的 a 乘入答案
            //
            // 举例：b = 13 = 1101(二进制)
            // 第一次循环：b & 1 = 1，最低位是1，所以 res *= a（此时 a = a^1）
            // 第二次循环：b >>= 1 后 b = 6 = 110，b & 1 = 0，跳过
            // 第三次循环：b >>= 1 后 b = 3 = 11，b & 1 = 1，所以 res *= a（此时 a = a^4）
            // 第四次循环：b >>= 1 后 b = 1 = 1，b & 1 = 1，所以 res *= a（此时 a = a^8）
            if ((b & 1) == 1) {
                // 乘法后立刻取模，防止数值溢出
                // 依据公式：(x * y) % p = ((x % p) * (y % p)) % p
                res = res * a % p;
            }

            // ---------- 步骤2：底数平方，准备下一位 ----------
            // 不管当前位是不是 1，底数都要平方
            // 因为下一位对应的指数是当前的 2 倍
            // 即：a^(2^i) -> a^(2^(i+1)) = (a^(2^i))^2
            //
            // 同样，平方后立刻取模
            a = a * a % p;

            // ---------- 步骤3：右移一位，处理下一个二进制位 ----------
            // b >>= 1 等价于 b = b / 2
            // 把二进制位向右移动一位，下次循环处理更高一位
            b >>= 1;
        }

        return res;
    }
}
